// Type definitions for design tokens
export type ColorScale = keyof typeof tokens.colors.primary
export type ColorName = keyof typeof tokens.colors
export type SpacingScale = keyof typeof tokens.spacing
export type FontSize = keyof typeof tokens.typography.fontSize
export type FontWeight = keyof typeof tokens.typography.fontWeight
export type Shadow = keyof typeof tokens.shadows
export type BorderRadius = keyof typeof tokens.borderRadius
export type Duration = keyof typeof tokens.motion.duration
export type Easing = keyof typeof tokens.motion.easing
export type ZIndex = keyof typeof tokens.zIndex
export type Breakpoint = keyof typeof tokens.breakpoints

// Helper functions for accessing tokens
export const getColor = (color: ColorName, scale: ColorScale): string => {
  const colorObject = tokens.colors[color] as Record<string, string>
  return colorObject[scale] || ''
}

export const getSpacing = (scale: SpacingScale) => 
  tokens.spacing[scale]

export const getFontSize = (size: FontSize) => 
  tokens.typography.fontSize[size]

export const getShadow = (shadow: Shadow) => 
  tokens.shadows[shadow]

export const getBorderRadius = (radius: BorderRadius) => 
  tokens.borderRadius[radius]

export const getDuration = (duration: Duration) => 
  tokens.motion.duration[duration]

export const getEasing = (easing: Easing) => 
  tokens.motion.easing[easing]

export const getZIndex = (zIndex: ZIndex) => 
  tokens.zIndex[zIndex]

export const getBreakpoint = (breakpoint: Breakpoint) => 
  tokens.breakpoints[breakpoint]